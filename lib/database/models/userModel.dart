class User {
  final int id;
  final String name;
  final String surname;
  final String midlename;
  final String role;
  final String password;
  final String login;

  const User({
    required this.id,
    required this.name,
    required this.surname,
    required this.midlename,
    required this.role,
    required this.password,
    required this.login,
  });
}